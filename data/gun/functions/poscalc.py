def pythagore(pointA: list[float], pointB: list[float]):
    """Takes 2 position as input and return the distance between both position"""
    distance = 0 
    pointB[1] += 1.32
    for i in range(len(pointA)): distance += abs(pointA[i]-pointB[i])**2
    return distance**(1/2)

